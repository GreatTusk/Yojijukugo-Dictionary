package modelo;

import java.io.Serializable;

/**
 *
 * @author f_776
 */
public class Yojijukugo implements Serializable {
    
    private int id;
    private String word;

    public Yojijukugo() {
    }

    public Yojijukugo(int id, String word) {
        this.id = id;
        this.word = word;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    
    
}
